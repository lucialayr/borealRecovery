library(here)
library(tidyverse)

cat("=== VALIDATING SUBSET RESULTS AGAINST DATA/FINAL ===\n\n")

# Load sampled grid cells
sampled_cells = read_csv(here("data", "processed", "sampled_grid_cells.csv"), 
                         show_col_types = FALSE)
cat("Sampled grid cells:", nrow(sampled_cells), "\n\n")

# Function to validate a file
validate_file = function(file_name, key_cols = c("Lon", "Lat", "PID")) {
  
  cat("Validating:", file_name, "\n")
  
  # Read reference data
  ref_path = here("data", "final", file_name)
  if (!file.exists(ref_path)) {
    cat("  ✗ Reference file not found\n\n")
    return(FALSE)
  }
  ref_data = read_csv(ref_path, show_col_types = FALSE)
  
  # Read subset data (generated by processing scripts)
  subset_path = here("data", "final", file_name)
  if (!file.exists(subset_path)) {
    cat("  ✗ Subset result file not found (not yet generated)\n\n")
    return(FALSE)
  }
  subset_data = read_csv(subset_path, show_col_types = FALSE)
  
  # Filter reference data to sampled cells
  if (all(key_cols %in% names(ref_data))) {
    ref_filtered = ref_data %>%
      semi_join(sampled_cells, by = c("Lon", "Lat"))
    
    cat("  Reference rows (all data):", nrow(ref_data), "\n")
    cat("  Reference rows (sampled cells):", nrow(ref_filtered), "\n")
    cat("  Subset rows:", nrow(subset_data), "\n")
    
    # Check if row counts match
    if (nrow(ref_filtered) != nrow(subset_data)) {
      cat("  ✗ Row count mismatch!\n\n")
      return(FALSE)
    }
    
    # Join and compare values
    comparison = ref_filtered %>%
      inner_join(subset_data, by = key_cols, suffix = c("_ref", "_subset"))
    
    # Check all value columns
    value_cols = setdiff(names(ref_filtered), key_cols)
    
    all_match = TRUE
    for (col in value_cols) {
      ref_col = paste0(col, "_ref")
      subset_col = paste0(col, "_subset")
      
      if (ref_col %in% names(comparison) && subset_col %in% names(comparison)) {
        # Allow for small numerical differences
        if (is.numeric(comparison[[ref_col]])) {
          max_diff = max(abs(comparison[[ref_col]] - comparison[[subset_col]]), na.rm = TRUE)
          if (max_diff > 1e-10) {
            cat("  ✗", col, ": max difference =", max_diff, "\n")
            all_match = FALSE
          }
        } else {
          n_mismatch = sum(comparison[[ref_col]] != comparison[[subset_col]], na.rm = TRUE)
          if (n_mismatch > 0) {
            cat("  ✗", col, ":", n_mismatch, "mismatches\n")
            all_match = FALSE
          }
        }
      }
    }
    
    if (all_match) {
      cat("  ✓ All values match exactly!\n\n")
      return(TRUE)
    } else {
      cat("  ✗ Some values do not match\n\n")
      return(FALSE)
    }
    
  } else {
    cat("  ⚠ Cannot validate (missing key columns)\n\n")
    return(NA)
  }
}

# List of files to validate
files_to_validate = c(
  "trajectories_mean_A_mean_2015_2040.csv",
  "trajectories_mean_A_mean_2075_2100.csv",
  "trajectories_mean_A_agc_2015_2040.csv",
  "trajectories_mean_A_agc_2075_2100.csv",
  "maps_regression_B_patches_2015_2040.csv",
  "maps_regression_B_patches_2075_2100.csv",
  "random_forest_A_2015_2040.csv",
  "random_forest_A_2075_2100.csv",
  "validation_B.csv"
)

cat("Files to validate:\n")
for (f in files_to_validate) {
  cat("  -", f, "\n")
}
cat("\n")

# Run validation
results = sapply(files_to_validate, validate_file)

cat("\n=== VALIDATION SUMMARY ===\n")
cat("Total files:", length(results), "\n")
cat("Validated:", sum(results, na.rm = TRUE), "\n")
cat("Failed:", sum(!results, na.rm = TRUE), "\n")
cat("Cannot validate:", sum(is.na(results)), "\n")
